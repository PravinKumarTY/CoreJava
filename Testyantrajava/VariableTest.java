public class VariableTest{
  public static void main(String []arg){
	  int i;//declaration
	  i=10;//initialization
	  System.out.println(i);//utilization
	  byte b;//declaration
	  b=120;//initialization
	  System.out.println(b);//initialization
	  short s;//declaration
	  s=123;//initialization
	  System.out.println(s);
	  double d;//declaration
	  d=1234;//initialization
	  System.out.println(d);//utilization
	  
	  int a=10;//declaration and initialization
	  System.out.println(a);
	  byte b1=20;//declaration and initialization
	  System.out.println(b1);
  }
}